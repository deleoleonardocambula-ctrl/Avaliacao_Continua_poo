    public void carregarDados(){
        modelo.setRowCount(0);//
        ArrayList<Animal> animais = Animal.lerAnimal();
        
        for(Animal animal: animais){
            Object[] linha = {animal.getNome(), animal.getIdade(), animal.fazerSom()};
            modelo.addRow(linha);
        }      
    }
