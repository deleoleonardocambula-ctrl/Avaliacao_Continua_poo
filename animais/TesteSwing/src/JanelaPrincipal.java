    private void btAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAddMouseClicked
        // TODO add your handling code here:
        String tipoAnimal = (String) cb.getSelectedItem();
        String nome = tfNome.getText();
        int idade = Integer.parseInt(tfIda.getText());

        if(tipoAnimal.equalsIgnoreCase("Cão")){
            Animal animal = new Cao(nome, idade);
            Animal.gravarUsuario(animal);
        }else{
            Animal animal = new Gato(nome, idade);
            Animal.gravarUsuario(animal);
        }
        
        carregarDados();
        
    }//GEN-LAST:event_btAddMouseClicked

